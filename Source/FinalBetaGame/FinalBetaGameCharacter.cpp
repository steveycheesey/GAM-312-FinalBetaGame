bool AFinalBetaGameCharacter::PlayFlash()
{
	if (RedFlash)
	{
		RedFlash = false;
		return true;
	}

	return false;
}

float AFinalBetaGameCharacter::TakeDamage(float DamageAmount, FDamageEvent const& DamageEvent, AController* EventInstigator, AActor* DamageCauser)
{
	SetCanBeDamaged(false);
	RedFlash = true;
	UpdateHealth(-DamageAmount);
	DamageTimer();
	return DamageAmount;
}


void AFinalBetaGameCharacter::UpdateHealth(float HealthChange)
{
	Health = FMath::Clamp(Health, 0.0f, FullHealth);
	HealthPercentage = Health / FullHealth;
} 
